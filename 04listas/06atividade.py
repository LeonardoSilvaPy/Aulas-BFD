'''
Crie uma lista chamada números com os valores [1, 2, 3, 2, 4, 2, 5]. Mostre quantas vezes o número 2 aparece na lista.
'''

# Criando a lista
numeros = [1, 2, 3, 2, 4, 2, 5]

# Contando quantas vezes o número 2 aparece
quantidade = numeros.count(2)

# Exibindo o resultado
print(f"O número 2 aparece {quantidade} vezes na lista.")
