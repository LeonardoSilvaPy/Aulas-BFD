'''
 Adicione mais dois livros à lista livros usando o método append() e exiba a lista atualizada.
'''

# Lista inicial
minha_lista = [
    "Musashi", "Sussurro", "Amos Daragon"
]

# Adicionando mais dois livros
minha_lista.append("Dom Quixote")
minha_lista.append("O Hobbit")

# Exibindo a lista atualizada
print(minha_lista)