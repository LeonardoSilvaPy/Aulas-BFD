'''
Dado o dicionário:

pessoa = {"nome": "Carlos", "idade": 30}
Adicione uma nova chave "cidade" com valor "São Paulo".
'''

# Dicionário inicial
pessoa = {"nome": "Carlos", "idade": 30}

# Adicionando a nova chave "cidade"
pessoa["cidade"] = "São Paulo"

# Exibindo o dicionário atualizado
print(pessoa)
