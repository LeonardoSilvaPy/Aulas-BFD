'''
Exercício 7: Calculadora simples
    Crie um programa que receba dois números digitados pelo usuário com input() e mostre o
    resultado das quatro operações básicas.
'''

n1 = int((input("Informe um numero: ")))
n2 = int((input("Informe um numero: ")))
print("Soma", n1+n2)
print("Subtração", n1-n2)
print("Multiplicação", n1*n2)
print("Divisão", n1/n2)