'''
Crie uma lista que contenha diferentes tipos de dados (string, inteiro, outra
lista...)
'''
# Criando uma lista com diferentes tipos de dados
minha_lista = [
    "Texto",          # String
    42,               # Inteiro
    3.14,             # Número decimal (float)
    True,             # Booleano
    [1, 2, 3],        # Outra lista
    {"nome": "Ana"},  # Dicionário
    (7, 8, 9)         # Tupla
]

# Exibindo a lista
print(minha_lista)

