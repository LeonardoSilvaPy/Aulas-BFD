'''
Use o slice para criar uma nova lista que inclua apenas os elementos com índice
3 e 4 da lista a seguir:

[ 1,2,3,4,5,6 ]

Resultado esperado: [4,5]
'''
# Lista original
lista = [1, 2, 3, 4, 5, 6]

# Criando a nova lista usando slice
nova_lista = lista[3:5]  # Pega os elementos de índice 3 e 4

# Exibindo o resultado
print(nova_lista)
